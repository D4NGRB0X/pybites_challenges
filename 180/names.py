def group_names_by_country(data: str = data) -> defaultdict:
    countries = defaultdict(list)
    # your code
    data_dict = [datapoint.split(',')[::-1] for datapoint in data.split('\n')[1:]]
    for k, v1, v2 in data_dict:
        countries[k].append(" ".join((v1, v2)))
    return countries
